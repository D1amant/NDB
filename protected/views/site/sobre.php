<p>
Era uma tarde fria de junho, no ano de 1996. O pagode romântico embalava o BRASIL e ditava a moda nas rádios e nas TVs, 
porém um grupo de adolescentes contrariava a moda, faziam um samba de roda com músicas antigas e com coreografias que encantavam a 
todos no point da cidade de São Paulo, o disputadíssimo, Consulado da Cerveja. Os meninos transmitiam em seus sorrisos o nome do grupo
 ARTE DE AMAR, e era evidente que o amor embalava aqueles adolescentes que se conheciam desde crianças, e o sucesso da época caía como uma 
 luva na roda de samba. Quando eu me sentava para tocar com eles aquela batucada parecia um sonho...
</p>
<p>
"A gente se fala no olhar... no olhar! 
É água de chuva no mar... no mar! 
Caminha pro mesmo lugar, 
Sem pressa sem medo de errar 
É tão bonito"
</p>
<p>
E bonito mesmo era ver que a cada sábado a feijoada ficava mais disputada, filas dobravam quarteirões, tudo para ver e ouvir uma turma que parecia estar num baile quando tocava. Foi assim minha paixão por estes meninos, que me ouviram, em 2001, quando sugeri que o grupo mudasse seu nome para TURMA DO PAGODE. Convencê-los foi difícil, mas como já tinha na manga a idéia de gravar um CD aí tudo ficou mais fácil, escolha de repertório, gravação, lançamento, pude acompanhar e ver de perto que entre tantas mudanças veio a maturidade, o profissionalismo e o BRASIL, tão carente de novidade artística não resistiu e abraçou esta TURMA como se abraça os amigos do bairro, da escola, do trabalho e principalmente do PAGODE, tenho em mim a certeza que valeu todo esforço ao ver o povo ecoar as músicas do grupo e como diz FERNANDO PESSOA, "TUDO VALE A PENA , QUANDO A ALMA NÃO É PEQUENA".
</p>
<p>
E para o bem de todos tem muita coisa boa por vir, senhoras e senhores sintam-se abraçados por estes que fazem de sua arte a arte de amar o samba.
</p>

